# Type hints

string_variable = "Hello, Python!"
print(string_variable) # Hello, Python!
print(type(string_variable)) # <class 'str'>

string_variable = 16
print(string_variable) # 16
print(type(string_variable)) # <class 'int'>

typed_string_variable: str = "Hello, FastAPI!"
print(typed_string_variable) # Hello, FastAPI!
print(type(typed_string_variable)) # <class 'str'>

typed_string_variable = 23
print(typed_string_variable) # 23
print(type(typed_string_variable)) # <class 'int'>
